    private void btnDownloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDownloadActionPerformed
        // TODO add your handling code here:
        DBconnection dbc = new DBconnection();
        
        DefaultTableModel model = (DefaultTableModel) tblDownload.getModel();
        String fileName = (String) model.getValueAt(tblDownload.getSelectedRow(), 2);
        
        //System.out.println(fileName);
        
        String queryToGetSelectedRowData = "SELECT * FROM course_contents WHERE file_name = '" + fileName + "';";
        try {
            Connection con = dbc.getConnect();
            
            PreparedStatement preparedStat = con.prepareStatement(queryToGetSelectedRowData);
            ResultSet rs = preparedStat.executeQuery();
            
            rs.next();
            
            Clob clob = rs.getClob(4);
            Reader reader = clob.getCharacterStream();
            FileWriter writer = new FileWriter("C:\\Users\\smsak\\Downloads\\file.txt");
            
            int i;
            while((i = reader.read())!= -1){
                writer.write((char) i);
            }
            
            writer.close();
            preparedStat.close();
            con.close();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(DownCourseCont.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(DownCourseCont.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDownloadActionPerformed

