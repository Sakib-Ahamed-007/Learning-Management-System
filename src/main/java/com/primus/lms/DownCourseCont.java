    public void setTableData(ArrayList<CourseContents> courseContents){
        DefaultTableModel model = (DefaultTableModel) tblDownload.getModel();
        
        for(int i = 0; i < courseContents.size(); i++){
            model.insertRow(tblDownload.getRowCount(), new Object[]{
                courseContents.get(i).getCourseName(),
                courseContents.get(i).getCourseID(),
                courseContents.get(i).getFileName(),
                courseContents.get(i).getDate()
        });
        }
    }

